		new_notes_location = "notes_subdir",

		link = {
			style = "wiki",
		},

		-- Daily notes are generated by the Python script.
		daily_notes = {
			folder = "journal",
			date_format = "%Y-%m-%d",
			alias_format = "%B %-d, %Y",
			-- template = "daily", -- Disabled: Use Python script for date-aware generation
		},

		templates = {
			folder = "templates",
			date_format = "%Y-%m-%d",
			time_format = "%H:%M",
			-- Substitutions for templates
			substitutions = {
				yesterday = function()
					return os.date("%Y-%m-%d", os.time() - 86400)
				end,
				tomorrow = function()
					return os.date("%Y-%m-%d", os.time() + 86400)
				end,
