			name = "telescope.nvim",
		},

		-- Human-readable note IDs that match the vault naming scheme
		note_id_func = function(title)
			local suffix = os.date("%Y%m%d%H%M")
			if title then
				local slug = title:lower():gsub("%s+", "-"):gsub("[^a-z0-9-]", "")
				slug = slug:gsub("-+", "-"):gsub("^-", ""):gsub("-$", "")
				return slug ~= "" and slug or suffix
			else
				return suffix
			end
		end,

